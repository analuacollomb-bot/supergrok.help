export const articles: Article[] = articleSeeds.map((seed) => ({
  ...seed,
  content: buildContent(seed),
  faqs: buildFaqs(seed),
}));

export function getArticleBySlug(slug: string) {
  return articles.find((article) => article.slug === slug);
}

export function getRelatedArticles(article: Article) {
  return article.relatedArticles
    .map((slug) => getArticleBySlug(slug))
    .filter((item): item is Article => Boolean(item));
}

export function articleToPlainText(article: Article) {
  return article.content
    .flatMap((section) => [section.heading, ...section.paragraphs, section.subheading, section.subparagraph])
    .join("\n");
}
